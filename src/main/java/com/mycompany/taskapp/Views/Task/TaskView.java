    private void btnHapusActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnHapusActionPerformed
        // TODO add your handling code here:
        Task _task = getTask("hapus");
        int confirm = JOptionPane.showConfirmDialog(null, "Yakin ingin menghapus task " + _task.getJudul() + "?");
        if (_task != null && confirm == 0) {
            taskController.delete(_task);
            initData();
            JOptionPane.showMessageDialog(null, "Task " + _task.getJudul() + " berhasil dihapus!");
        }
    }//GEN-LAST:event_btnHapusActionPerformed

    private Task getTask(String mode) {
        int selectedRow = TodoTable.getSelectedRow();
        if (selectedRow < 0) {
            JOptionPane.showMessageDialog(null, "Pilh baris yang di " + mode + "!");
            return null;
        }
        String _id = TodoTable.getValueAt(selectedRow, 0).toString();
        Task _task = taskController.readById(_id);
        return _task;
    }

    public void initData() {
        ArrayList<Task> TodoData = taskController.readAll();
        List<String[]> values = new ArrayList<String[]>();

        for (Task data : TodoData) {
            values.add(new String[]{data.getId(), data.getJudul(), data.getDeskripsi()});
        }

        TodoTable.setModel(new javax.swing.table.DefaultTableModel(
                values.toArray(new Object[][]{}),
                new String[]{
                    "ID", "TITLE", "DESCRIPTION"
                }
        ));

    }

